    public void displayItemStatisticsTillToday() {
        StatisticsForDashBoardService statisticsService = new StatisticsForDashBoardService();
        List<StatisticsForDashBoard> pList = statisticsService.getItemStatisticsTillToday();
        System.out.println("Size: " + pList.size());
        DefaultTableModel model = (DefaultTableModel) tblDisplayStockSummary.getModel();
        Object[] row = new Object[6];

        for (int i = 0; i < pList.size(); i++) {
            row[0] = pList.get(i).getItemName();
            row[1] = pList.get(i).getProductCode();
            row[2] = pList.get(i).getBrandOrCategory();
            row[3] = pList.get(i).getTotalItemIn();
            row[4] = pList.get(i).getTotalItemOut();
            row[5] = pList.get(i).getTotalItemAvailable();

            model.addRow(row);
        }

    }
    
