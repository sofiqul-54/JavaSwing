    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        PaymentService paymentService = new PaymentService();
       
        //int studentID, String name, String fatherOrMother, String mobile, String className, double monthlyFee, int noOfMonth, String monthName, double totalPaidAmount, String payDate
        Payment payment = new Payment(Integer.parseInt(txtStdID.getText()), txtName.getText(), txtFather.getText(), txtMobile.getText(), txtClassName.getText(), Double.parseDouble(txtFee.getText()), Integer.parseInt(txtNoOfMonth.getText()), txtMonth.getText(),Double.parseDouble(txtFee.getText()), txtDate.getText());
        paymentService.save(payment);
        lblMsg.setText("success!!");

        //for book summary
        FeeSummaryService pss = new FeeSummaryService();
        
        List list = pss.getList();
        int sizeOfList = list.size();
        // if (sizeOfList == 0) {

        try {
            FeeSummary bs = pss.getFeeSummaryByStudentID(Integer.parseInt(txtStdID.getText()));
            
            if (bs != null) {
                if (bs.getStudentID() == Integer.parseInt(txtStdID.getText())) {

// data from db
                    // after modification
                    double totalPaid= bs.getPaidFee() + Double.parseDouble(txtTotalAmount.getText());
                    double dueFee = bs.getDueFee() - Double.parseDouble(txtTotalAmount.getText());
                    
                    FeeSummary bsx = new FeeSummary();
                    bsx.setPaidFee(totalPaid);
                    bsx.setDueFee(dueFee);
                    bsx.setId(bs.getId());
                    
                    pss.updateWhenPay(bsx);
                    
                    refreshTable();
                    
                } else {
                    //(int studentID, String name, double totalPaybleFee, double paidFee, double dueFee)
                    FeeSummary bsxyz = new FeeSummary(Integer.parseInt(txtStdID.getText()), txtName.getText(), Double.parseDouble(txtFee.getText()), 0.0, Double.parseDouble(txtFee.getText()));
                    pss.save(bsxyz);
                    
                    refreshTable();
                    
                }
            } else {
                FeeSummary bsxyz = new FeeSummary(Integer.parseInt(txtStdID.getText()), txtName.getText(), Double.parseDouble(txtFee.getText()), 0.0, Double.parseDouble(txtFee.getText()));
                pss.save(bsxyz);
                
                refreshTable();
                
            }
        } catch (Exception e) {
            FeeSummary bsxyz = new FeeSummary(Integer.parseInt(txtStdID.getText()), txtName.getText(), Double.parseDouble(txtFee.getText()), 0.0, Double.parseDouble(txtFee.getText()));
            pss.save(bsxyz);
            
            refreshTable();
            
        }
        
        refreshTable();
        

    }//GEN-LAST:event_btnaddActionPerformed
    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
        displayDataWithinTable();
    }
    
    public void displayDataWithinTable() {
        StudentService studentService = new StudentService();
        List<Student> pList = studentService.getList();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[10];
        
        for (int i = 0; i < pList.size(); i++) {
            row[0] = pList.get(i).getId();
            row[1] = pList.get(i).getStudentID();
            row[2] = pList.get(i).getName();
            row[3] = pList.get(i).getFatherOrMother();
            row[4] = pList.get(i).getMobile();
            row[5] = pList.get(i).getClassName();
            row[6] = pList.get(i).getMonthlyFee();
            // row[7] = pList.get(i).getNoOfMonth();
            //row[8] = pList.get(i).getTotalFee();
            row[7] = pList.get(i).getAddmissionDate();
            
            model.addRow(row);
        }
        
    }
